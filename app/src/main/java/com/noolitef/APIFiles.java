package com.noolitef;

public class APIFiles {
    private byte[] device;
    private byte[] preset;
    private byte[] user;

    public byte[] getDevice() {
        return device;
    }

    public void setDevice(byte[] device) {
        this.device = device;
    }

    public byte[] getPreset() {
        return preset;
    }

    public void setPreset(byte[] preset) {
        this.preset = preset;
    }

    public byte[] getUser() {
        return user;
    }

    public void setUser(byte[] user) {
        this.user = user;
    }
}
